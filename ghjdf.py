from PIL import Image, ImageDraw

p = Image.new('RGB', (2800, 1000), 'black')
draw = ImageDraw.Draw(p)
draw.polygon(((0, 0), (600, 0), (600, 200), (400, 200),
              (400, 1000), (200, 1000), (200, 200), (0, 200)), 'red')

draw.polygon(((600, 0), (1200, 0), (1200, 1000), (1000, 1000),
              (1000, 700), (800, 700), (800, 1000), (600, 1000)), 'orange')
draw.rectangle(((800, 200), (1000, 500)), 'black')

draw.rectangle(((1200, 400), (1400, 1000)), 'yellow')
draw.rectangle(((1200, 0), (1400, 200)), 'yellow')

draw.polygon(((1400, 0), (2000, 0), (2000, 200), (1600, 200),
              (1600, 400), (2000, 400), (2000, 1000), (1400, 1000),
              (1400, 800), (1800, 800), (1800, 600), (1400, 600)), 'green')

draw.rectangle(((2000, 400), (2200, 1000)), 'aqua')
draw.rectangle(((2000, 0), (2200, 200)), 'aqua')

draw.polygon(((2200, 0), (2800, 0), (2800, 1000), (2600, 1000),
              (2600, 700), (2400, 700), (2400, 1000), (2200, 1000)), 'blue')
draw.rectangle(((2400, 200), (2600, 500)), 'black')
p.save('name.png')